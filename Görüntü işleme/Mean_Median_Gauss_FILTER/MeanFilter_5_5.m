function [F] =MeanFilter(I)
    [n,m,k] = size(I);
    F=I;
    for i1= 3:n-2
        for i2= 3:m-2
            for i3 = 1:k
                
                x=(     (I(i1-2,i2-2,i3)/24 + ...
                         I(i1-1,i2-2,i3)/24+...
                         I(i1,i2-2,i3)/24+...
                         I(i1+1,i2-2,i3)/24+...
                         I(i1+2,i2-2,i3)/24+...                         
                         I(i1-2,i2-1,i3)/24+...
                         I(i1-1,i2-1,i3)/24+...
                         I(i1,i2-1,i3)/24+...
                         I(i1+1,i2-1,i3)/24+...
                         I(i1+2,i2-1,i3)/24+... 
                         I(i1-2,i2,i3)/24+...
                         I(i1-1,i2,i3)/24+...
                         I(i1+1,i2,i3)/24+...
                         I(i1+2,i2,i3)/24+...             
                         I(i1-2,i2+1,i3)/24+...
                         I(i1-1,i2+1,i3)/24+...
                         I(i1,i2+1,i3)/24+...
                         I(i1+1,i2+1,i3)/24+...
                         I(i1+2,i2+1,i3)/24+...
                         I(i1-2,i2+2,i3)/24+...
                         I(i1-1,i2+2,i3)/24+...
                         I(i1+2,i2+2,i3)/24+....
                         I(i1,i2+2,i3)/24+...
                         I(i1+1,i2+2,i3)/24));
              F(i1,i2,i3) = (x);

            end
        end
    end

 
end

